    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        this.setVisible(false);
        
        String answer = new String();
        
        if(Alt1TextPane.isSelected()) {
            answer = Alt1TextPane.getText();
        }
        else if(Alt2TextPane.isSelected()) {
            answer = Alt2TextPane.getText();
        }
        else if(Alt3TextPane.isSelected()) {
            answer = Alt3TextPane.getText();
        }
        else if(Alt4TextPane.isSelected()) {
            answer = Alt4TextPane.getText();
        }
        else if(Alt5TextPane.isSelected()) {
            answer = Alt5TextPane.getText();
        }
        
        TheSurveyManager.saveAnswer(question, answer);
        
        TheSurveyManager.user_interface.nextQuestion();
    }//GEN-LAST:event_NextButtonActionPerformed

